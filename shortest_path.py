# Dijkstra's algorithm:
# objectives: to begin, have a dictionary with all node values, starting node will have a value of 0,
# and the rest will have an infinite value. the goal is to put the shortest path between the starting node
# to every node, along with the path in the dictionary when completed. there will be a queue with each node value, and 
# that value will be removed when the shortest path has been determined.
# infinity is float('inf')